<?php

/**
 * @param $numero
 * @return float|int
 */
    function duplicar($numero)
    {
        $resultado = $numero * 2;
        return $resultado;
    }

    /**
     * @param float|int $dividendo
     * @param float|int $divisor
     * @return float|int $resultado
     */
    function dividir( $dividendo, $divisor )
    {
        $resultado = $dividendo/$divisor;
        return $resultado;
    }

